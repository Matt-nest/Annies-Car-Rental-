// Images: transparent PNGs in /public/fleet/{VIN}/

// Helper to build image paths from VIN and available angles
function buildImages(vin: string, hero: boolean, side: boolean, rear: boolean) {
  const base = `/fleet/${vin}`;
  const heroPath = hero ? `${base}/hero.png` : null;
  const sidePath = side ? `${base}/side.png` : null;
  const rearPath = rear ? `${base}/rear.png` : null;
  return {
    image: heroPath!,
    images: [heroPath, sidePath, rearPath].filter(Boolean) as string[],
    heroImage: heroPath!,
    sideImage: sidePath,
    rearImage: rearPath,
  };
}

