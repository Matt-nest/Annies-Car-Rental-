// Helper: get display name for a vehicle
export function getVehicleDisplayName(v: Vehicle): string {
  return `${v.year} ${v.make} ${v.model}`;
}

// Helper: get vehicles by category
export function getVehiclesByCategory(category: string): Vehicle[] {
  if (category === 'all') return VEHICLES;
  return VEHICLES.filter(v => v.category === category);
}

// Helper: get a single vehicle by ID
export function getVehicleById(id: string): Vehicle | undefined {
  return VEHICLES.find(v => v.id === id);
}

// Helper: get a single vehicle by VIN
export function getVehicleByVin(vin: string): Vehicle | undefined {
  return VEHICLES.find(v => v.vin === vin);
}
